'''Αυτό το πρόγραμμα υπολογίζει και εμφανίζει την
   τετραγωνική ρίζα ενός αριθμού, καθώς και την
   ύψωσή του στη δύναμη του 3'''

import math                         # Εισαγωγή του αρθρώματος math
a=float(input('Δώσε αριθμό:'))      # Περιμένει από τον χρήστη έναν αριθμό
# Εμφανίζει την τετραγωνική ρίζα
print('Η τετραγωνική ρίζα του',a,'είναι',math.sqrt(a))
# Εμφανίζει ύψωση του αριθμού εις την 3η
print('To',a,'εις την 3η είναι',pow(a,3))


