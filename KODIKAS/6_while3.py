synolo=0
atoma=0
poso=1
while poso>=0:
    poso=float(input('Δώστε ότι έχετε ευχαρίστηση :'))
    if poso>=0:
        synolo=synolo+poso
        atoma=atoma+1
print('Στον έρανο συμμετείχαν',atoma,'άτομα')
print('και συγκεντρώθηκαν',synolo,'Ευρώ')
