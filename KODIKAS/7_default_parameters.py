def mult(a=0,b=1,c=1,d=1):
	ginomeno = a*b*c*d
	return ginomeno

# Κυρίως πρόγραμμα
print(mult())			# υπολογίζει τη παράσταση 0*1*1*1
print(mult(10))                 # υπολογίζει τη παράσταση 10*1*1*1
print(mult(10,2,3)) 		# υπολογίζει τη παράσταση 10*2*3*1
print(mult(10,2,3,5)) 	        # υπολογίζει τη παράσταση 10*2*3*5














